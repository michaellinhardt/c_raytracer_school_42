#include "raystruct.h"

static double	fade(double t)
{
	return (t * t * t * (t * (t * 6 - 15) + 10));
}

static double	lerp(double t, double a, double b)
{
	return (a + t * (b - a));
}

static double	grad(int hash, double x, double y, double z)
{
	int		h;
	double	u;
	double	v;

	h = hash & 15;
	u = (h < 8 || h == 12 || h == 13) ? x : y;
	v = (h < 4 || h == 12 || h == 13) ? y : z;
	return (((h & 1) == 0 ? u : -u) + ((h & 2) == 0 ? v : -v));
}

static double	init_param(int *i, double *u, int *permutation)
{
	i[0] = ((int)floor(u[3]) & 255);
	i[1] = ((int)floor(u[4]) & 255);
	i[2] = ((int)floor(u[5]) & 255);
	u[3] -= floor(u[3]);
	u[4] -= floor(u[4]);
	u[5] -= floor(u[5]);
	u[0] = fade(u[3]);
	u[1] = fade(u[4]);
	u[2] = fade(u[5]);
	i[7] = permutation[i[0]] + i[1];
	i[3] = permutation[i[7]] + i[2];
	i[4] = permutation[i[7] + 1] + i[2];
	i[7] = permutation[i[0] + 1] + i[1];
	i[6] = permutation[i[8]] + i[2];
	i[5] = permutation[i[8] + 1] + i[2];
	return (lerp(u[2], lerp(u[1], lerp(u[0], grad(permutation[i[3]], u[3],
	u[4], u[5]), grad(permutation[i[6]], u[3] - 1, u[4], u[5])), lerp(u[0],
	grad(permutation[i[4]], u[3], u[4] - 1, u[5]), grad(permutation[i[5]],
		u[3] - 1,
	u[4] - 1, u[5]))), lerp(u[1], lerp(u[0], grad(permutation[i[3] + 1], u[3],
	u[4], u[5] - 1), grad(permutation[i[6] + 1], u[3] - 1, u[4], u[5] - 1)),
	lerp(u[0], grad(permutation[i[4] + 1], u[3], u[4] - 1, u[5] - 1),
	grad(permutation[i[5] + 1], u[3] - 1, u[4] - 1, u[5] - 1)))));
}

